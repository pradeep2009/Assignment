package com.forloopassignment;

public class EvenInRange {

	public static void main(String[] args) {
		
		
		System.out.print("List of even numbers from 121 to 229: ");
		for (int i=121; i<=229; i++)   
		{   
		if (i%2==0)   
		{  
		System.out.println(i + " ");  
		}  
		}  

	}

}
