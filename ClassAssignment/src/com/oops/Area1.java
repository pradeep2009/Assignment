package com.oops;

public class Area1 {

	public double circleArea(int r)
	{
		return 3.14*r*r;
	}
	public float reactAngleArea(int l,int w)
	{
		return l*w;
	}
	public double triangleArea(int b,int h)
	{
		return 0.5*b*h;
	}
	public double squareArea(int side)
	{
		return side*side;
	}
}
